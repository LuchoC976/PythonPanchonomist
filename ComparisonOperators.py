# Comparison operators

# We can compare variables to define conditions
# using this list of operators:\
# = Asignacion
# == Igualdad
'''
== (equality) 
!= (disequality)
&& (and)
|| (or)
>= (greater or equal than)
> (greater than)
<= (lesser or equal than)
< (lesser than)
'''

# We can mix this operators with conditionals
var1 = 1
var2 = 1

if (var1 == var2) and not (var1 < var2):
    print("Caso1")
else:
    print("Caso2")




